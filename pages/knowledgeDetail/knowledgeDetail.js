  onLoad: function(options) {
    // 获取传递的参数
    const knowledgeId = parseInt(options.id)
    const category = options.category
    
    this.setData({
      knowledgeId: knowledgeId,
      category: category
    })
    
    // 设置页面标题
    wx.setNavigationBarTitle({
      title: '医学知识详情'
    })
    
    // 加载知识详情
    this.loadKnowledgeDetail()
  },
  
  // 加载知识详情
  loadKnowledgeDetail: function() {
    const { knowledgeId, category, detailedContent } = this.data
    
    if (detailedContent[category] && detailedContent[category][knowledgeId]) {
      this.setData({
        knowledge: detailedContent[category][knowledgeId]
      })
    } else {
      wx.showToast({
        title: '知识内容加载失败',
        icon: 'none'
      })
    }
  },
  
